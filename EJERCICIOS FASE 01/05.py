#Programa que muestre el mensaje “Próxima Clasificación” en una función, pero se debe repetir 20 veces. En la función NO usar bucles y máximo debe ser de 4 líneas. Usar while obligatoriamente.
def mostrar(cont=1):
    while cont!=21:
        print("-",cont,". Próxima Clasificación.")
        cont+=1
mostrar()